<?php

namespace App\Notifications;

use Illuminate\Bus\Queueable;
use Illuminate\Contracts\Queue\ShouldQueue;
use Illuminate\Notifications\Messages\MailMessage;
use Illuminate\Notifications\Notification;
use Illuminate\Foundation\Auth\User;

class userAccountVerified extends Notification
{
    use Queueable;

    public $user;

    /**
     * Create a new notification instance.
     *
     * @return void
     */
    public function __construct(User $user)
    {
        $this->user = $user;
    }

    /**
     * Get the notification's delivery channels.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function via($notifiable)
    {
        return ['mail'];
    }

    /**
     * Get the mail representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return \Illuminate\Notifications\Messages\MailMessage
     */
    public function toMail($notifiable)
    {
        $user = $this->user;

        return (new MailMessage)
                    ->subject('Account Activated Successfully')
                    ->greeting('Congratulations '. $user->name.'!')
                    ->line("Your Email Address has been successfully verified.")
                    ->line("Feel free to continue play in our site but remember there is another verify you must do. You have to verify your identity. ")
                    ->line("Once all verifications are completed, we will remove all restrictions on your account. To Submit verification document(s) now, click on the button below to login and edit your profile and also upload your photo.")
                    ->action('Verify Now', route('kyc.create'))
                    ->line('Thank you for using our website! If you face any problem feel free to contact us anytime.');
    }

    /**
     * Get the array representation of the notification.
     *
     * @param  mixed  $notifiable
     * @return array
     */
    public function toArray($notifiable)
    {
        return [
            //
        ];
    }
}
